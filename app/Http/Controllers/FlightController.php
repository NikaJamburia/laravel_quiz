    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $flight = new Flight();
        $flight->identification_code = $request['identification_code'];
        $flight->take_off_airport_name = $request['take_off_airport_name'];
        $flight->arrival_airport_name = $request['arrival_airport_name'];
        $flight->take_off_time = $request['take_off_time'];
        $flight->arrival_time = $request['arrival_time'];
        $flight->save();
        return redirect('/flights');
    }

